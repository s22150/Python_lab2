num1 = int (input("Podaj pirwsza liczbe "))
num2 = int (input("Podaj druga liczbe "))
operation = input("Podaj operacje ")
if(operation == "+"):
    print(num1+num2)
elif (operation == "-"):
    print(num1-num2)
elif (operation == "*"):
    print(num1 * num2)
elif (operation == "/"):
    print(num1 / num2)


